function today() {
  return new Date().toISOString().split("T")[0];
}

function formatDate(d) {
  return new Date(d + "T00:00:00").toLocaleDateString("en-IN", {
    weekday: "short",
    day: "numeric",
    month: "short",
    year: "numeric",
  });
}

function showTab(id) {
  document
    .querySelectorAll(".tab")
    .forEach((t, i) => t.classList.remove("active"));
  document
    .querySelectorAll(".section")
    .forEach((s) => s.classList.remove("active"));
  const tabs = document.querySelectorAll(".tab");
  const tabMap = { log: 0, week: 1, db: 2 };
  tabs[tabMap[id]].classList.add("active");
  document.getElementById("tab-" + id).classList.add("active");
  if (id === "week") renderWeekly();
}

function setupSearch() {
  const inp = document.getElementById("food-search");
  const results = document.getElementById("search-results");
  inp.addEventListener("input", () => {
    const q = inp.value.trim().toLowerCase();
    if (!q) {
      results.style.display = "none";
      return;
    }
    const matches = FOODS.filter(
      (f) => String(f.sno).includes(q) || f.name.toLowerCase().includes(q),
    ).slice(0, 20);
    if (!matches.length) {
      results.style.display = "none";
      return;
    }
    results.innerHTML = matches
      .map(
        (f) =>
          `<div class="search-result-item" onclick="selectFood(${f.sno})">
        <span class="sno">#${f.sno}</span><strong>${f.name}</strong>
        <span style="color:#7a7a76; font-size:11px; float:right;">${f.cat} · ${f.e || "?"} kcal</span>
      </div>`,
      )
      .join("");
    results.style.display = "block";
  });
  document.addEventListener("click", (e) => {
    if (!inp.contains(e.target) && !results.contains(e.target))
      results.style.display = "none";
  });
}

